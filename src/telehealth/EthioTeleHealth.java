    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (!this.headache.isSelected() && !this.cold.isSelected() && !this.bp.isSelected() && !this.diabetes.isSelected() && this.symptoms.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please tell us what you are feeling!");
        } else {
            Patient p = User.getPatient(PatID2.getText());
            HealthRecord record = new HealthRecord(LocalDate.now(), p.getName());
            if (this.headache.isSelected()) {
                record.addSymptom("headache");
            }
            if (this.cold.isSelected()) {
                record.addSymptom("Cold");
            }
            if (this.bp.isSelected()) {
                record.addSymptom("blood pressure");
            }
            if (this.diabetes.isSelected()) {
                record.addSymptom("Diabetes");
            }

            if (!this.symptoms.getText().equals("")) {
                record.addSymptom(this.symptoms.getText());
            }
            p.getHealthRecord().add(record);
            JOptionPane.showMessageDialog(this, "We have received your symptoms successfully\nwe will reach you out as soon as possible\nplease be safe!");
            DefaultTableModel model = (DefaultTableModel) this.patientTable.getModel();
            Object[] row = {p.getName(), p.getId(), p.getGender(), record.getSymptom()};
            model.addRow(row);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

