    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) this.patientTable.getModel();

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "You don't have any patient right now!");
        } else if (this.patientTable.getSelectedRowCount() > 1) {
            JOptionPane.showMessageDialog(this, "Please select one patient");
        } else if (this.patientTable.getSelectedRowCount() == 1) {
            Patient p = User.getPatient((String)model.getValueAt(patientTable.getSelectedRow(), 1));
            if (p != null) {
                
                
                this.patientName.setText(p.getName());
                DefaultTableModel mod = (DefaultTableModel) this.pHistory.getModel();
                while(pHistory.getRowCount() > 0){
                    mod.removeRow(0);
                }
                for (int i = 0; i < p.getHealthRecord().size(); i++) {
                    Object[] row = {p.getHealthRecord().get(i).getSymptom().toString(), p.getHealthRecord().get(i).getMedication(), p.getHealthRecord().get(i).getDate()};
                    mod.addRow(row);
                }
                
                
                welcome.setSelectedIndex(7);
            }
        }

    }//GEN-LAST:event_jButton9ActionPerformed

