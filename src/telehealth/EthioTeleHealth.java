    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if(this.prescription.getText().equals(""))
            JOptionPane.showMessageDialog(this,"Please put prescription first");
        else{
            Patient p = User.getPatinetUsingName(this.subscriptionPatient.getText());
            p.getHealthRecord().get(patientTable.getSelectedRow()).addMedications(this.prescription.getText());
            int choice = JOptionPane.showConfirmDialog(this, "You are subscribing the following medication\n" + this.prescription.getText() +"\n"
                    + "are you sure to continue?", "Confirmation", JOptionPane.YES_NO_OPTION);
            if(choice == JOptionPane.YES_OPTION){
                welcome.setSelectedIndex(4);    
        }}
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        if(this.EmeregencyTable.getSelectedRowCount() == 1 && this.reviewName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please fill your name!");
        }
        else if(this.EmeregencyTable.getSelectedRowCount() == 1){
            EmeregencyTable.removeRowSelectionInterval(this.EmeregencyTable.getSelectedRow(),this.EmeregencyTable.getSelectedRow());
            JOptionPane.showMessageDialog(this, "Review Successful");
            welcome.setSelectedIndex(0);
        }
        else{
            welcome.setSelectedIndex(0);
        }
        
    }//GEN-LAST:event_jButton8ActionPerformed

